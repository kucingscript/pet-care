public class MakananKambing {
    private String merk, harga;
       
    public MakananKambing(String merk, String harga) {
        this.merk = merk;
        this.harga = harga;
    }
    
    public String getMerk() {
        return merk;
    }
    
    public String getHarga() {
        return harga;
    }
}
